const buildScenarioSource = ({
  scenario = {},
  actorIndex = 0,
} = {}) => {
  const actorId = `shadow_fixture_${scenario.id}_${String(actorIndex + 1).padStart(3, "0")}`;
  const goalStack = buildGoalStack(scenario);
  const baseTime = Date.UTC(2026, 1, 2, 12, 0, 0) + ((actorIndex + 1) * DAY_MS);
  const planStage = {
    currentPhase: "BUILD",
    currentWeek: 4,
    currentDay: scenario.recommendationKind === ADAPTIVE_RECOMMENDATION_KINDS.weeklyPlanRefresh ? 0 : 2,
    dateKey: new Date(baseTime).toISOString().split("T")[0],
    planWeekId: `${scenario.id}_week_4`,
    planDayId: `${scenario.id}_day_${String(actorIndex + 1).padStart(3, "0")}`,
  };
  const recommendationEvent = createRecommendationEvent({
    actorId,
    occurredAt: baseTime,
    scenario,
    goalStack,
    planStage,
  });
  const recommendationJoinKey = recommendationEvent.payload.recommendationJoinKey;
  const decisionId = recommendationEvent.payload.decisionId;
  const events = [
    createCohortSnapshotEvent({ actorId, occurredAt: baseTime - (14 * DAY_MS), scenario }),
    createUserStateSnapshotEvent({ actorId, occurredAt: baseTime - (13 * DAY_MS), scenario, goalStack, planStage }),
    createWeeklyEvaluationEvent({
      actorId,
      occurredAt: baseTime - (7 * DAY_MS),
      weekNumber: 3,
      planStage,
      adherenceRate: 0.62,
      painFlags: 0,
      goalProgressSignal: "Baseline week before the shadow decision.",
      linkedRecommendationJoinKeys: [],
    }),
    recommendationEvent,
    createOutcomeEvent({
      actorId,
      occurredAt: baseTime + DAY_MS,
      recommendationJoinKey,
      decisionId,
      scenario,
    }),
    createWeeklyEvaluationEvent({
      actorId,
      occurredAt: baseTime + (10 * DAY_MS),
      weekNumber: 5,
      planStage,
      adherenceRate: scenario.shortAdherence,
      painFlags: scenario.painFlags,
      goalProgressSignal: scenario.goalProgressSignal,
      linkedRecommendationJoinKeys: [recommendationJoinKey],
    }),
    createWeeklyEvaluationEvent({
      actorId,
      occurredAt: baseTime + (35 * DAY_MS),
      weekNumber: 8,
      planStage,
      adherenceRate: scenario.mediumAdherence,
      painFlags: scenario.painFlags,
      goalProgressSignal: scenario.goalProgressSignal,
      linkedRecommendationJoinKeys: [recommendationJoinKey],
    }),
  ];

  return {
    sourceId: actorId,
    sourceType: "shadow_policy_fixture",
    adaptiveLearning: {
      actorId,
      userId: "",
      seq: events.length,
      events,
      pendingEventIds: [],
      lastLocalWriteAt: baseTime + (35 * DAY_MS),
      lastCloudReadAt: baseTime + (35 * DAY_MS),
      lastCloudWriteAt: baseTime + (35 * DAY_MS),
      lastReplayAt: baseTime + (35 * DAY_MS),
    },
  };
};

