const buildGoalStack = (scenario = {}) => ([
  {
    id: `${scenario.id}_goal_primary`,
    summary: `${scenario.primaryGoalCategory} priority`,
    category: scenario.primaryGoalCategory,
    priority: 1,
    active: true,
  },
  ...toArray(scenario.secondaryGoalCategories).slice(0, 2).map((category, index) => ({
    id: `${scenario.id}_goal_secondary_${index + 1}`,
    summary: `${category} support`,
    category,
    priority: index + 2,
    active: true,
  })),
]);

const createRecommendationEvent = ({
  actorId = "",
  occurredAt = 0,
  recommendationKind = "",
  chosenOption = {},
  goalStack = [],
  planStage = {},
  contextualInputs = {},
  whyChosen = [],
  provenance = {},
  sourceSurface = "today",
  owner = "planning",
} = {}) => {
  const recommendationJoinKey = buildRecommendationJoinKey({
    recommendationKind,
    planWeekId: planStage.planWeekId,
    planDayId: planStage.planDayId,
    dateKey: planStage.dateKey,
    weekNumber: planStage.currentWeek,
    chosenOption,
    fallbackSeed: `${actorId}|${chosenOption?.optionKey || chosenOption?.label || ""}`,
  });
  const decisionId = buildAdaptiveDecisionId({
    recommendationKind,
    recommendationJoinKey,
    actorId,
    occurredAt,
    fallbackSeed: chosenOption?.optionKey || chosenOption?.label || "",
  });
  return createAdaptiveLearningEvent({
    eventName: ADAPTIVE_LEARNING_EVENT_NAMES.recommendationGenerated,
    actorId,
    localActorId: actorId,
    occurredAt,
    dedupeKey: `${recommendationKind}_${recommendationJoinKey}`,
    payload: {
      recommendationKind,
      recommendationJoinKey,
      decisionId,
      goalStack,
      planStage,
      contextualInputs,
      candidateOptionsConsidered: [{
        optionKey: chosenOption.optionKey,
        label: chosenOption.label,
        source: chosenOption.source,
        details: chosenOption.details,
      }],
      chosenOption,
      whyChosen,
      confidence: "",
      provenance,
      sourceSurface,
      owner,
    },
  });
};

const createOutcomeEvent = ({
  actorId = "",
  occurredAt = 0,
  recommendationJoinKey = "",
  decisionId = "",
  outcomeKind = "",
  adherenceOutcome = ADAPTIVE_ADHERENCE_OUTCOMES.unknown,
  completionPercentage = 0,
  frustrationSignal = "",
  painFlag = false,
  actualSummary = "",
  sourceSurface = "log",
  owner = "logging",
} = {}) => createAdaptiveLearningEvent({
  eventName: ADAPTIVE_LEARNING_EVENT_NAMES.recommendationOutcomeRecorded,
  actorId,
  localActorId: actorId,
  occurredAt,
  dedupeKey: `${outcomeKind}_${recommendationJoinKey}`,
  payload: {
    outcomeKind,
    recommendationJoinKey,
    decisionId,
    adherenceOutcome,
    completionPercentage,
    userModifications: frustrationSignal ? [frustrationSignal] : [],
    perceivedDifficulty: painFlag ? "harder_than_expected" : "about_right",
    painFlag,
    painArea: painFlag ? "general_fatigue" : "",
    satisfactionSignal: completionPercentage >= 0.8 ? "positive" : completionPercentage <= 0.45 ? "negative" : "neutral",
    frustrationSignal,
    shortHorizonResultWindow: {
      windowDays: outcomeKind === ADAPTIVE_OUTCOME_KINDS.coachAccepted || outcomeKind === ADAPTIVE_OUTCOME_KINDS.coachIgnored ? 7 : 3,
      reviewDateKey: new Date(occurredAt).toISOString().split("T")[0],
      observedSignals: [adherenceOutcome, frustrationSignal].filter(Boolean),
      summary: actualSummary,
    },
    actualSummary,
    sourceSurface,
    owner,
  },
});

